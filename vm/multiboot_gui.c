/* Draw text string using Chicago font */
static void draw_text(const char* str, int x, int y, uint8_t color) {
    /* PROV: Character spacing based on System 7.1 Chicago font metrics */
    int pos = x;
    while (*str) {
        draw_char_chicago(*str, pos, y, color);
        /* Chicago font typical spacing: char width + 1px spacing */
        if (*str == 'i' || *str == 'l' || *str == '!' || *str == '.') {
            pos += 4;  /* Narrow characters */
        } else if (*str >= 'A' && *str <= 'Z') {
            pos += 8;  /* Capital letters */
        } else {
            pos += 7;  /* Default spacing */
        }
        str++;
    }
}

/* Draw Mac OS menubar */
